/// <summary>
/// Типы блоков в мире
/// </summary>
public enum BlockType : byte
{
    Air = 0,        // Воздух (пустота)
    Stone = 1,      // Камень
    Dirt = 2,       // Земля
    Grass = 3,      // Трава
    Water = 4,      // Вода
    Wood = 5,       // Дерево (ствол)
    Leaves = 6,     // Листва
    Sand = 7,       // Песок
    Gravel = 8,     // Гравий
    Clay = 9        // Глина
}

